$table .= "<tr><td>Broj obojenih paragrafa: </td><td>".$paragraphsColored."</td></tr>";
if ($paragraphsColored > 0) {
	$html = str_replace("<p", "<p style=\"color:#00f;\"", $html);
}

// podvlacimo strong delove
$strongElements = substr_count($html, "<strong>");
$table .= "<tr><td>Broj podvucenih strong elemenata: </td><td>".$strongElements."</td></tr>";
if ($strongElements > 0) {
	$html = str_replace("<strong>", "<strong><u>", $html);
	$html = str_replace("</strong>", "</u></strong>", $html);
}

// tekstovi linkova velikim slovima
$table .= "<tr><td>Broj promenjenih tekstova linkova: </td>";
$cnt = 0;
$pos = strpos($html, "<a ");
while ($pos !== false) {
	$startOfText = strpos($html, ">", $pos) + 1;
	$endOfText = strpos($html, "</", $startOfText);
	
	$replacement = strtoupper(substr($html, $startOfText, $endOfText-$startOfText));
	$html = substr_replace($html, $replacement, $startOfText, $endOfText-$startOfText);
	$cnt++;
	
	$pos = strpos($html, "<a ", $endOfText);
}
$table .= "<td>".$cnt."</td></tr>";

// uklanjamo id-jeve ul elemenata
$table .= "<tr><td>Broj obrisanih id-jeva listi: </td>";
$cnt = 0;
$pos = strpos($html, "<ul ");
while ($pos !== false) {
	$start = strpos($html, "id", $pos);
	$ul_end = strpos($html, ">", $pos);
	if ($start !== false && $start < $ul_end) {
		$first_apostrophe = strpos($html, "'", $start);
		$second_apostrophe = strpos($html, "'", $first_apostrophe+1);
		$html = substr_replace($html, "", $start, $second_apostrophe-$start+1);
		$cnt++;
	}
	$pos = strpos($html, "<ul ", $pos + 1);
}
$table .= "<td>".$cnt."</td></tr>";

